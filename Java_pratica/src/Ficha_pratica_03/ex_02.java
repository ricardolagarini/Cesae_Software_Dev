package Ficha_pratica_03;

public class ex_02 {
    public static void main(String[] args) {

        int numero;

        numero = 2;

        // enquanto o contador for menor que 400, vai continuar a imprimir numero +2

        while (numero <= 400) {
            System.out.println(numero);
            numero = numero +2;
        }

    }
}
